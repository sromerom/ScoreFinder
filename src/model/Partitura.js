class Nota {
  constructor(
    id,
    titol,
    idiomaOriginal,
    idiomaDesti,
    lletraOriginal,
    lletraDesti,
    notes
  ) {
    this.id = id;
    this.titol = titol;
    this.idiomaOriginal = idiomaOriginal;
    this.idiomaDesti = idiomaDesti;
    this.lletraOriginal = lletraOriginal;
    this.lletraDesti = lletraDesti;
    this.notes = notes;
  }
}
